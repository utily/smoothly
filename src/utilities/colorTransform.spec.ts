import { toCommaRgb } from "./colorTransform"

describe("colorTransform", () => {
	it("commaRgb unformated", () => {
		expect(toCommaRgb("0, 0, 0")).toEqual("0,0,0")
		expect(toCommaRgb("255, 255, 255")).toEqual("255,255,255")
		expect(toCommaRgb("255, 0, 0")).toEqual("255,0,0")
		expect(toCommaRgb(" 0,255, 0")).toEqual("0,255,0")
		expect(toCommaRgb("0,255,0,255")).toEqual(undefined)
	})
	it("hexToCommaRgb", () => {
		expect(toCommaRgb("#000000")).toEqual("0,0,0")
		expect(toCommaRgb(" #ffffff")).toEqual("255,255,255")
		expect(toCommaRgb("#ff0000")).toEqual("255,0,0")
		expect(toCommaRgb("#00FF00")).toEqual("0,255,0")
		expect(toCommaRgb("#00FF0")).toEqual(undefined)
	})
	it("hexToCommaRgb with short hex", () => {
		expect(toCommaRgb("#000")).toEqual("0,0,0")
		expect(toCommaRgb("#FFF")).toEqual("255,255,255")
		expect(toCommaRgb(" #f00")).toEqual("255,0,0")
		expect(toCommaRgb("#0F0")).toEqual("0,255,0")
		expect(toCommaRgb("#ffff")).toEqual(undefined)
	})
	it("rgbToCommaRgb", () => {
		expect(toCommaRgb("rgb(0,0,0)")).toEqual("0,0,0")
		expect(toCommaRgb("rgb(255,255,255)")).toEqual("255,255,255")
		expect(toCommaRgb("RGB(255,0,0)")).toEqual("255,0,0")
		expect(toCommaRgb("rgb(0,255,0)")).toEqual("0,255,0")
		expect(toCommaRgb("rgb(-0,0,0)")).toEqual(undefined)
		expect(toCommaRgb("rgb(0,+30,0)")).toEqual(undefined)
		expect(toCommaRgb("rgb(256,255,255)")).toEqual(undefined)
		expect(toCommaRgb("rgb(255,0,0,0)")).toEqual(undefined)
	})
	it("hsl", () => {
		expect(toCommaRgb("hsl(0, 100%,50%)")).toEqual("255,0,0")
		expect(toCommaRgb("hsl(120, 100%,50%)")).toEqual("0,255,0")
		expect(toCommaRgb("hsl(240,100%,50%)")).toEqual("0,0,255")
		expect(toCommaRgb("hsl(240,100%,0%)")).toEqual("0,0,0")
		expect(toCommaRgb("hsl(180,100%, 50%)")).toEqual("0,255,255")
		expect(toCommaRgb("hsl(240,100%,100%)")).toEqual("255,255,255")
	})
	it("colorNames", () => {
		expect(toCommaRgb("red")).toEqual("255,0,0")
		expect(toCommaRgb("green")).toEqual("0,128,0")
		expect(toCommaRgb("blue")).toEqual("0,0,255")
		expect(toCommaRgb("yellow ")).toEqual("255,255,0")
		expect(toCommaRgb(" black ")).toEqual("0,0,0")
		expect(toCommaRgb("white")).toEqual("255,255,255")
		expect(toCommaRgb("magenta")).toEqual("255,0,255")
		expect(toCommaRgb("cyan")).toEqual("0,255,255")
		expect(toCommaRgb("navy")).toEqual("0,0,128")
	})
})
